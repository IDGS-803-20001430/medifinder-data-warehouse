-- Definición de la sentencia BULK INSERT para importar datos desde CSV a SQL Server
BULK INSERT medifinder.dbo.Medico
FROM 'C:\ruta\al\archivo\medico_cleaned.csv'  -- Ruta absoluta al archivo CSV en tu sistema
WITH (
    FIELDTERMINATOR = ',',  -- Delimitador de campos en el archivo CSV
    ROWTERMINATOR = '\n',    -- Delimitador de filas en el archivo CSV (nueva línea)
    FIRSTROW = 2             -- Indica que la primera fila contiene datos (omitir encabezados)
);


